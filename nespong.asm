  .inesprg 1
  .ineschr 1
  .inesmir 1
  .inesmap 0

  .bank 0
  .org $8000

;--------------------------------------------------------------------------------------------------------------
; Startup code
;--------------------------------------------------------------------------------------------------------------
Reset:
  ; Disable IRQs and decimal mode
  sei
  cld

  ; Disable API IRQ
  ldx #$40
  stx $4017

  ; Setup stack
  ldx #$FF
  txs